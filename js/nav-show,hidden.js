/*nav1*/
$('.main-nav > li:nth-child(1)').mouseenter(function() {
    $('.sub-nav1').css({
        opacity: 1,
        visibility: 'visible'
    })
});
$('.main-nav > li:nth-child(1)').mouseleave(function() {
    $('.sub-nav1').css({
        opacity: 0,
        visibility: 'hidden'
    })
});


